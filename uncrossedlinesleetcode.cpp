int help(int i, int j, vi &a, vi &b){
	if(i==0 or j==0) return 0;	
	if(dp[i][j]!=-1) return dp[i][j];
	if(a[i-1]==b[j-1]) return dp[i][j] = (help(i-1,j-1,a,b))+1;
	return dp[i][j] = max(help(i-1,j,a,b), help(i,j-1,a,b));
}
